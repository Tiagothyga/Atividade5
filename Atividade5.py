# Faça um algoritmo que ajude uma empresa de limpeza. Para isso o programa deve ler a largura e 
# o comprimento de um cômodo e calcular e mostrar a área a ser limpa e a quantidade de produto 
# necessária para o serviço, sabendo que cada litro de produto limpa uma área de 2 metros quadrados

print ("Bem vindo ☺️")
x = float(input("Digite a largura do cômodo: "))
y = float(input("Digite o comprimento do cômodo: "))
z = x * y
a = z / 2

print ("A area a ser limpa e de",z , "metros quadrados")
print ("E a quantidade de produto para a limeza de area e de", a, "letros")